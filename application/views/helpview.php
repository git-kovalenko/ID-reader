<h2>Отчеты</h2>
<p> При нажатии на ссылки в этом разделе - на сервере создается соответствующий файл Excel 97-2003 и появляется окно сохранения этого файла, уже названного соответствующим именем, включающим дату. 
<h3>Отчет за период (включительно)</h3>
Эта ссылка остается неактивной пока не будет выбран клиент и период формируемого отчета (в днях).<br>
<br>
Отчеты можно делать сколько угодно раз, они не влияют на содержание базы даных.
</p>

